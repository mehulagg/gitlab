<script>
  import newModal from './modal.vue';
  import upload from './upload.vue';
  import icon from '../../../vue_shared/components/icon.vue';

  export default {
    props: {
      projectId: {
        type: String,
        required: true,
      },
      branch: {
        type: String,
        required: true,
      },
      path: {
        type: String,
        required: true,
      },
      parent: {
        type: Object,
        default: null,
      },
    },
    components: {
      icon,
      newModal,
      upload,
    },
    data() {
      return {
        openModal: false,
        modalType: '',
      };
    },
    methods: {
      createNewItem(type) {
        this.modalType = type;
        this.toggleModalOpen();
      },
      toggleModalOpen() {
        this.openModal = !this.openModal;
      },
    },
  };
</script>

<template>
  <div class="repo-new-btn pull-right">
    <div class="dropdown">
      <button
        type="button"
        class="btn btn-sm btn-default dropdown-toggle add-to-tree"
        data-toggle="dropdown"
        aria-label="Create new file or directory"
      >
        <icon
          name="plus"
          :size="12"
          css-classes="pull-left"
        />
        <icon
          name="arrow-down"
          :size="12"
          css-classes="pull-left"
        />
      </button>
      <ul class="dropdown-menu dropdown-menu-right">
        <li>
          <a
            href="#"
            role="button"
            @click.prevent="createNewItem('blob')"
          >
            {{ __('New file') }}
          </a>
        </li>
        <li>
          <upload
            :project-id="projectId"
            :branch-id="branch"
            :path="path"
            :parent="parent"
          />
        </li>
        <li>
          <a
            href="#"
            role="button"
            @click.prevent="createNewItem('tree')"
          >
            {{ __('New directory') }}
          </a>
        </li>
      </ul>
    </div>
    <new-modal
      v-if="openModal"
      :type="modalType"
      :project-id="projectId"
      :branch-id="branch"
      :path="path"
      :parent="parent"
      @toggle="toggleModalOpen"
    />
  </div>
</template>
